package Product.CommandStorage;

import Product.Receivers.ReceiverStorage;

public class Add_if_min extends AbstractCommand{
    public Add_if_min(ReceiverStorage receiverStorage, String name, int argumentNumber) {
        super(receiverStorage, name, argumentNumber);
    }

    @Override
    public void execute(String[] argument) {
        checkCollectionManager();
        accessCollectionManager.add_if_min(getTerminalInterpreter().getScanner());
    }

    @Override
    public String getDescription() {
        return "add_if_min {element} : добавить новый элемент в коллекцию, если его значение меньше, чем у наименьшего элемента этой коллекции";
    }
}
